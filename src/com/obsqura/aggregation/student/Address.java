package com.obsqura.aggregation.student;

public class Address {
	String houseName;
	String city;
	String state;
	int pin;
	public Address(String houseName, String city, String state, int pin) {
		super();
		this.houseName = houseName;
		this.city = city;
		this.state = state;
		this.pin = pin;
	}
	
}
